 <h2 class="pageID">Flexbox</h2>
 <p>Flexbox

      You will be able to use Flexbox module, when you trying to make a dynamic layout, adjust and assign space between element in a container. Especially, when items size are unknown and changing, hence the word “flex”. The fundamental idea about flex is to allow the container, the capacity of changing its item’s width/height to take the best way and to use the space that's available. 
        Flexbox which is a new way for developers to create layouts inside their websites. For the past many years developers have been using something called float, inside CSS in order to take elements inside a website and place them next to each other. Float was really intended to be something that developers not supposed to be used that way. But because it worked, developers have been using it for so many years, so luckily now we have a couple of new methods we can actually use in order to create layouts inside websites. one being CSS grid and another one being flex box.
     Flex bars is more available inside the different browsers. flexbox is more about rearranging the sub items inside sections of a website. If you have a stylesheet that has some default styling inside of it that simply gets rid of margins and padding inside the website. Flex gets rid of these unwanted margins that we have inside the browser's as a default. Flex has a class called container. Where you could set width to full width, height to 400 pixels, and then the background color to red. You could actually see it inside the website.
       You could use flexbox to create some styling for the container. if you want to make the container section into a flex box, you will then give it a display set to flex. Which then tells it that you are going to have some items inside this container that you want to rearrange in a certain way. Another thing you need to add to a flex box is to tell it if it should wrap the content onto multiple lines. Incase if you have a bunch of text inside this website that goes in a different boxes that goes horizontally. If there's no more room horizontally then it should jump down to the next line. This is something that float actually does as a default, but inside flexbox you do actually need to tell to wrap the next line.


